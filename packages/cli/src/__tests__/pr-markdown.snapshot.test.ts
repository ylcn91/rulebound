describe("renderPrMarkdown — product boundary language", () => {
  // Guards that the report cannot collapse the deterministic / advisory
  // distinction. These are independent of any single snapshot so a future
  // wording change can update snapshots without silently weakening the
  // contract.
  const report: DeterministicReport = {
    status: "FAILED",
    summary: { total: 3, pass: 0, violated: 3, notApplicable: 0, error: 0, blocking: 1, waived: 1 },
    ruleStatuses: [{ ruleId: "advisory.r", title: "advisory.r", checkCount: 0, status: "ADVISORY", blocking: false }],
    parseErrors: [],
    results: [
      {
        ruleId: "det.blocker",
        checkId: "regex:secret",
        status: "VIOLATED",
        source: "regex",
        deterministic: true,
        confidence: "exact",
        blocking: true,
        reason: "blocker",
      },
      {
        ruleId: "det.warning",
        checkId: "regex:todo",
        status: "VIOLATED",
        source: "regex",
        deterministic: true,
        confidence: "high",
        blocking: false,
        reason: "warning",
      },
      {
        ruleId: "waived.rule",
        checkId: "regex:legacy",
        status: "VIOLATED",
        source: "regex",
        deterministic: true,
        confidence: "exact",
        blocking: true,
        reason: "waived blocker",
        waived: { reason: "tracked", expires: "2099-01-01" },
      },
    ],
    waiversApplied: [
      {
        result: {
          ruleId: "waived.rule",
          checkId: "regex:legacy",
          status: "VIOLATED",
          source: "regex",
          deterministic: true,
          confidence: "exact",
          blocking: true,
          reason: "waived blocker",
        },
        waiver: { rule: "waived.rule", reason: "tracked", owner: "@alice", expires: "2099-01-01" },
        expired: false,
      },
    ],
  }
  const out = renderPrMarkdown(report, "rulebound check")

  it("keeps deterministic blockers, warnings, waivers, analyzer findings, and repair as distinct sections", () => {
    expect(out).toContain("### Deterministic blockers")
    expect(out).toContain("### Deterministic warnings")
    expect(out).toContain("### Waivers applied")
    expect(out).toContain("### Analyzer findings")
    expect(out).toContain("### Repair")
  })

  it("surfaces advisory-only rule count in the summary so they cannot be confused with blockers", () => {
    expect(out).toContain("**Advisory-only rules:** 1")
  })

  it("never lists a waived finding under deterministic blockers", () => {
    const blockersSection = out.split("### Deterministic blockers")[1]?.split("###")[0] ?? ""
    expect(blockersSection).not.toContain("waived.rule")
  })

  it("includes the repair command verbatim under the Repair section", () => {
    expect(out).toMatch(/### Repair[\s\S]+```bash\nrulebound check\n```/)
  })
})
