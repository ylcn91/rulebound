import { renderPrMarkdown } from "../commands/check.js"

// Deterministic snapshot fixtures for the pr-markdown renderer.
//
// Rules for stable snapshots:
// - no Date.now / current timestamps in fixtures,
// - no absolute paths (only repo-relative),
// - explicit, stable result ordering (we list results in the order they
//   appear in the array — the renderer preserves input order).
//
// The renderer takes the rerun hint as an argument, so snapshots also
// fix that string instead of letting CLI options leak in.
const RERUN = "rulebound check --base main"

function emptySummary(): DeterministicReport["summary"] {
  return { total: 0, pass: 0, violated: 0, notApplicable: 0, error: 0, blocking: 0, waived: 0 }
}

function makeResult(overrides: Partial<CheckResult> & Pick<CheckResult, "ruleId" | "checkId" | "status">): CheckResult {
  return {
    source: "regex",
    deterministic: true,
    confidence: "exact",
    blocking: true,
    reason: "",
    ...overrides,
  } as CheckResult
}

function makeRuleStatus(overrides: Partial<RuleStatus> & Pick<RuleStatus, "ruleId">): RuleStatus {
  return {
    title: overrides.ruleId,
    checkCount: 1,
    status: "PASS",
    blocking: true,
    ...overrides,
  } as RuleStatus
}

function makeWaiver(overrides: Partial<Waiver> & Pick<Waiver, "rule" | "expires">): Waiver {
  return {
    reason: "tracked",
    owner: "@team",
    ...overrides,
  } as Waiver
}

function makeReport(input: {
  status: DeterministicReport["status"]
  results?: readonly CheckResult[]
  ruleStatuses?: readonly RuleStatus[]
  parseErrors?: readonly { ruleId: string; errors: readonly string[] }[]
  waiversApplied?: readonly AppliedWaiver[]
  summary?: Partial<DeterministicReport["summary"]>
}): DeterministicReport {
  return {
    status: input.status,
    results: input.results ?? [],
    ruleStatuses: input.ruleStatuses ?? [],
    parseErrors: input.parseErrors ?? [],
    waiversApplied: input.waiversApplied ?? [],
    summary: { ...emptySummary(), ...(input.summary ?? {}) },
  }
}

